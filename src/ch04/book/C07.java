package ch04.book;

public class C07 {
	public static void main(String[] args) {
		for (int i=1; i <=5; i++) {
			System.out.println(i);
		}
		System.out.println("");

		for (int j = 5; j <=10; j++) {
			System.out.println(j);
		}
		System.out.println("");

		for(int z = 5; z >=1; z--) {
			System.out.println(z);
		}
		System.out.println("");

		for(int z = 10; z >=5; z--) {
			System.out.println(z);
		}
	}
}
